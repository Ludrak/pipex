#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	lsrc;

	if (!dst || !src)
		return (0);
	i = 0;
	lsrc = ft_strlen(src);
	if (dstsize <= 0)
		return (lsrc);
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (lsrc);
}
